    /**
     * Adds a text t to the text field or increases the number of that text by one if its already at the end of a string
     * @param t the text to add
     */
    public void addText(String t) {
        String f = formulaTextField.getText();
        // If the text field is empty, set it to t
        if (f.equals("")) {
            formulaTextField.setText(t);
        }
        // if the textfield length is less than t, add t because t cant be in it
        else if (f.length() < t.length()) {
            formulaTextField.setText(f + t);
        }
        // if the last 2 characters are a t, add "2" to the end of the text field's string
        else if (f.substring(f.length() - t.length(), f.length()).equals(t)) {
            formulaTextField.setText(f + "2");
        }
        // if the last number is a digit, increase that digit by one if the String becfore it is t, otherwise add t
        else if (Formatting.isDigit(f.charAt(f.length() - 1))) {
            String num = "";
            int at = f.length() - 1;
            // find the point at which the digits start
            for (int i = f.length() - 1; i >= 0; i--) {
                if (!Formatting.isDigit(f.charAt(i))) {
                    at = i + 1;
                    // increase the digit by one
                    num = Formatting.addOneToString(f.substring(i + 1));
                    i = -1;
                }
            }
            // if the characters before the digit are t, increase the digit by one
            if (at - t.length() >= 0 && f.substring(at - t.length(), at).equals(t)) {
                formulaTextField.setText(f.substring(0, at) + num);
            }
            // else append t to the end
            else {
                formulaTextField.setText(f + t);
            }
        }
        // add t if everything else does nothing
        else {
            formulaTextField.setText(f + t);
        }
    }
