    private void calculateTheMolarMassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateTheMolarMassActionPerformed
        String text = formulaTextField.getText();
        try {
            // Remove all illegal characters from the string
            text = Formatting.removeIllegalChar(text);
            finder = new MassFinder(text);
            
            // Use only 3 places after the decimal
            String mm = String.format("%.3f", finder.getMolarMass());
            
            // Set output
            String output = "The Molar Mass of " + text + " is: " + mm + "\n";
            String[] elements = finder.getElements();
            double[] percents = finder.percentComposition();
            for (int i = 0; i < elements.length; i++) {
                String s = "The Composition of " + elements[i] + " is: ";
                String pc = "%";
                output = String.format("%s%s%.3f%s\n", output, s, percents[i], pc);
            }
            // Print output
            outputTextArea.setText(output);
        } catch(UnsupportedOperationException ex) {
            outputTextArea.setText(ex.getMessage());
        }
    }//GEN-LAST:event_calculateTheMolarMassActionPerformed

